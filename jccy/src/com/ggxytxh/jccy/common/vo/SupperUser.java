package com.ggxytxh.jccy.common.vo;

public class SupperUser {

	private Integer id;
	private String supperuser_id;
	private String password;
	private Integer type_id;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getSupperuser_id() {
		return supperuser_id;
	}

	public void setSupperuser_id(String supperuser_id) {
		this.supperuser_id = supperuser_id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public Integer getType_id() {
		return type_id;
	}

	public void setType_id(Integer type_id) {
		this.type_id = type_id;
	}
}
